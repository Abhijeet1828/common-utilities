package com.custom.common.utilities.constants;

/**
 * Java class to store common constants across multiple Spring Boot Projects.
 * 
 * @author Abhijeet
 *
 */
public final class Constants {

	/**
	 * Creating a final class with exception if the class is instantiated somewhere
	 * else in the code.
	 */
	private Constants() {
		throw new IllegalStateException("Constants class cannot be instantiated");
	}

}
